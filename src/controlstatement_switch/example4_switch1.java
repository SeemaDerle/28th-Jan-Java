package controlstatement_switch;

public class example4_switch1 
{
public static void main(String[] args)
{
	String inp="CD";
	switch(inp)
	{
	case "CD":
		System.out.println("running MS code");
		break;
	case "CW":
		System.out.println("running MS code");
		break;
	case "BI":
		System.out.println("running MS code");
		break;
	case "MS":
		System.out.println("running MS code");
		break;
		default:System.out.println("wrong input");
		
	}
}
}

